package prism14;

public class ScorecardPartnerPenetrationValues {

	private double actual, threshold;
	
	public ScorecardPartnerPenetrationValues(double actual, double threshold) {
		this.actual = actual;
		this.threshold = threshold;
	}
	
	public double getActual() {
		return this.actual;
	}

	public double getThreshold() {
		return this.threshold;
	}

}
